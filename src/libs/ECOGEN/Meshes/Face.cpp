#include "Face.h"

//***********************************************************************

Face::Face(){}

//***********************************************************************

Face::~Face(){}

//***********************************************************************

Coord Face::vecteur(Element *e)
{
  Coord vec;
  vec.setFromSubtractedVectors(m_position, e->getPosition());
  return vec;
}

//***********************************************************************

double Face::distance(Element *e)
{
  Coord vec(m_position - e->getPosition());
  return vec.norm();
}

